package principal;

/**
 * Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle while.
 *
 */
public class S05Ejercicio02 {

    public static void main(String[] args) {
        int contador = 0;

        while (contador <= 100) {
            System.out.println(contador);
            contador += 5;
        }

    } // Fin main

} // Fin class
